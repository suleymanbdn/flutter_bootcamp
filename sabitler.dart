void main(){
  //Sabitler
  //final, const
  int sayi = 30;
  print(sayi);
  sayi = 100;
  print (sayi);
  
  // final: kodlamayı çalıştırdığımızda hafızada oluşur.
  final int numara = 100;
  print(numara);
  //numara = 200; hata
  
  
  /* const: değişkeni tanımladığımızda hafızada oluşur.
     !!!genellikle görsel nesnelerde kullanırız!!!
   */
  const int sonuc = 200;
  print(sonuc);
  //sonuc = 300; hata 
}
